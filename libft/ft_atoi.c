#include "libft.h"

int	ft_atoi(char *str)
{
	int n;
	int i;
	int flag;

	n = 0;
	i = 0;
	flag = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			flag = -1;
	while (*str >= '0' && *str <= '9')
	{
		n = (n * 10) + (*str++ - '0');
		if (i > 19)
		{
			if (flag < 1)
				return (0);
			return (-1);
		}
		i++;
	}
	return (n * flag);
}
